package unidade1.exemplos;

public class doWhile {
    public static void main(String[] args) {
        int contador = 1;
        while (contador <= 5) { // a condição é verificada antes de cada iteração loop, e o bloco de codigo dentro do while é repetido enquanto a condição for verdadeira
            System.out.println("Contagegem : " + contador);
            contador ++;
        }

    }
}
