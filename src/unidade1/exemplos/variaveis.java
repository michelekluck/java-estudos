package unidade1.exemplos;
/*
 crie um algoritmo em Java contendo uma variável do tipo int, outra
 do tipo float e outra do tipo String. A variável do tipo float precisa
 conter um valor. O algoritmo também precisa ter duas constantes: uma do tipo
 float, e outra do tipo String.
*/
public class variaveis {
    public static void main(String[] args) {
        //Exemplo de declaração e atribuição de variavel
        int numero; // numero (tipo: int)
        float altura = 1.70f; // altura(tipo: float, valor inidial: 1.70)
        String nome; // nome(tipo: String)

        //Exemplos de declração e atribuição de constante
        final float PI = 3.1416F; // PI (tipo: float, valor constante: 3.1416)
        final String NOME_PAGINA = "home"; // NOME_PAGINA(tipo: String, valor constante: "home")
    }
}
